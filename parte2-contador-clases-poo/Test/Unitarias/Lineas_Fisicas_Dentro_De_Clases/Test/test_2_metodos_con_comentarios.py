class operaciones:
    def suma(a,b):
        """
        Esta es una funcion que realiza y retorna una suma
        Parametros de entrada
        a (int), b (int)
        Salida
        a + b
        """

        return a + b

    def resta(a,b):
        """
        Esta es una funcion que realiza y retorna una resta
        Parametros de entrada
        a (int), b (int)
        Salida
        a - b
        """
        return a - b
